-- Extend invoices for livreur recipients
ALTER TABLE public.invoices
  ADD COLUMN IF NOT EXISTS recipient_type text NOT NULL DEFAULT 'vendeur',
  ADD COLUMN IF NOT EXISTS livreur_id uuid,
  ADD COLUMN IF NOT EXISTS notes text;

ALTER TABLE public.invoices ALTER COLUMN vendeur_id DROP NOT NULL;

DO $$ BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'invoices_recipient_consistency') THEN
    ALTER TABLE public.invoices
      ADD CONSTRAINT invoices_recipient_consistency CHECK (
        (recipient_type = 'vendeur' AND vendeur_id IS NOT NULL AND livreur_id IS NULL)
        OR (recipient_type = 'livreur' AND livreur_id IS NOT NULL AND vendeur_id IS NULL)
      );
  END IF;
END $$;

-- Extend invoice items with editable per-order metadata
ALTER TABLE public.invoice_items
  ADD COLUMN IF NOT EXISTS product_name text,
  ADD COLUMN IF NOT EXISTS tracking_number text,
  ADD COLUMN IF NOT EXISTS customer_city text,
  ADD COLUMN IF NOT EXISTS status_snapshot text;

-- Livreurs can view their own invoices
DROP POLICY IF EXISTS "Livreurs see own invoices" ON public.invoices;
CREATE POLICY "Livreurs see own invoices" ON public.invoices
  FOR SELECT TO authenticated USING (livreur_id = auth.uid());

