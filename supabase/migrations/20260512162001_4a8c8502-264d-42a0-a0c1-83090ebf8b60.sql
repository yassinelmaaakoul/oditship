-- Invoice schedules table
CREATE TABLE IF NOT EXISTS public.invoice_schedules (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  recipient_type text NOT NULL UNIQUE CHECK (recipient_type IN ('vendeur','livreur')),
  enabled boolean NOT NULL DEFAULT false,
  frequency_days integer NOT NULL DEFAULT 30 CHECK (frequency_days > 0),
  last_run_at timestamptz,
  next_run_at timestamptz,
  updated_at timestamptz NOT NULL DEFAULT now(),
  updated_by uuid
);

ALTER TABLE public.invoice_schedules ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Admins manage invoice schedules" ON public.invoice_schedules;
CREATE POLICY "Admins manage invoice schedules" ON public.invoice_schedules
  FOR ALL TO authenticated
  USING (has_role(auth.uid(), 'administrateur'::app_role))
  WITH CHECK (has_role(auth.uid(), 'administrateur'::app_role));

DROP POLICY IF EXISTS "Comptables manage invoice schedules" ON public.invoice_schedules;
CREATE POLICY "Comptables manage invoice schedules" ON public.invoice_schedules
  FOR ALL TO authenticated
  USING (has_role(auth.uid(), 'comptable'::app_role))
  WITH CHECK (has_role(auth.uid(), 'comptable'::app_role));

-- Seed default schedule rows
INSERT INTO public.invoice_schedules (recipient_type, enabled, frequency_days)
VALUES ('vendeur', false, 30), ('livreur', false, 30)
ON CONFLICT (recipient_type) DO NOTHING;

CREATE TRIGGER trg_invoice_schedules_updated
BEFORE UPDATE ON public.invoice_schedules
